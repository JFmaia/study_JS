/* 
    Type conversion (type casting) vs Type Coersion
    * Alterações de um tipo de dado para outro.
*/

// Exemplo:

// Type coersion
console.log('9'+ 5); // exibe 95 transformando tudo automaticamente em string.

// Type conversion
console.log(Number('9') + 5); // exibe 14, pois o operador + é do tipo number.